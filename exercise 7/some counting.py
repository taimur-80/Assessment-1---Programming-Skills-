# Count up from 0 to 50
for d in range(51):
    print(d)

# Count down from 50 to 0
for d in range(50, -1, -1):
    print(d)

# Count up from 30 to 50
for d in range(30, 51):
    print(d)

# Count down from 50 to 10 in decrements of 2
for d in range(50, 9, -2):
    print(d)

# Count up from 100 to 200 in increments of 5
for d in range(100, 201, 5):
    print(d)